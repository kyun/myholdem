// get x,y from 2d array when value is 1
export const getXY = (arr: number[][]) => {
  const result: number[][] = [];
  arr.forEach((row, x) => {
    row.forEach((value, y) => {
      if (value === 1) {
        result.push([x + 1, y + 1]);
      }
    });
  });
  return result;
};
